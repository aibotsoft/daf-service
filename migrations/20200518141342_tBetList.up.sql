    EventId     int,
    MarketName  varchar(1000),
    SideName  varchar(1000),
    Points      decimal(9, 5),
    EventTime   datetimeoffset(0),
    Status      varchar(1000),
    WinLoss     decimal(9, 5),
    Price       decimal(9, 5),
    Stake       decimal(9, 5),
    BetTypeId   int,
    BetTime     datetimeoffset(0),
    WinLoseDate datetimeoffset(0),
    primary key (Id)
)