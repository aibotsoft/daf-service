create or alter proc dbo.uspSaveSide @Id bigint,
                                     @SideIndex tinyint,
                                     @SportName varchar(1000),
                                     @SportId int,
                                     @LeagueName varchar(1000),
                                     @LeagueId int,
                                     @Home varchar(1000),
                                     @HomeId int,
                                     @Away varchar(1000),
                                     @AwayId int,
                                     @MarketName varchar(1000),
                                     @MarketId int,
                                     @Price decimal(9, 5),
                                     @Initiator bit,
                                     @Starts datetimeoffset,
                                     @EventId int,
                                     @CheckId bigint,
                                     @AccountLogin varchar(1000),
                                     @CheckPrice decimal(9, 5),
                                     @CheckStatus varchar(1000),
                                     @CountLine int,
                                     @CountEvent int,
                                     @AmountEvent int,
                                     @AmountLine int,
                                     @MinBet decimal(9, 5),
                                     @MaxBet decimal(9, 5),
                                     @Balance int,
                                     @Currency decimal(9, 5),
                                     @CheckDone bigint,
                                     @CalcStatus varchar(1000),
                                     @MaxStake decimal(9, 5),
                                     @MinStake decimal(9, 5),
                                     @MaxWin decimal(9, 5),
                                     @Stake decimal(9, 5),
                                     @Win decimal(9, 5),
                                     @IsFirst bit
as
begin
    set nocount on

    MERGE dbo.Side AS t
    USING (select @Id,
                  @SideIndex,
                  @SportName,
                  @SportId,
                  @LeagueName,
                  @LeagueId,
                  @Home,
                  @HomeId,
                  @Away,
                  @AwayId,
                  @MarketName,
                  @MarketId,
                  @Price,
                  @Initiator,
                  @Starts,
                  @EventId,
                  @CheckId,
                  @AccountLogin,
                  @CheckPrice,
                  @CheckStatus,
                  @CountLine,
                  @CountEvent,
                  @AmountEvent,
                  @AmountLine,
                  @MinBet,
                  @MaxBet,
                  @Balance,
                  @Currency,
                  @CheckDone,
                  @CalcStatus,
                  @MaxStake,
                  @MinStake,
                  @MaxWin,
                  @Stake,
                  @Win,
                  @IsFirst) s (Id,
                               SideIndex,
                               SportName,
                               SportId,
                               LeagueName,
                               LeagueId,
                               Home,
                               HomeId,
                               Away,
                               AwayId,
                               MarketName,
                               MarketId,
                               Price,
                               Initiator,
                               Starts,
                               EventId,
                               CheckId,
                               AccountLogin,
                               CheckPrice,
                               CheckStatus,
                               CountLine,
                               CountEvent,
                               AmountEvent,
                               AmountLine,
                               MinBet,
                               MaxBet,
                               Balance,
                               Currency,
                               CheckDone,
                               CalcStatus,
                               MaxStake,
                               MinStake,
                               MaxWin,
                               Stake,
                               Win,
                               IsFirst)
    ON (t.Id = s.Id and t.SideIndex=s.SideIndex)

    WHEN MATCHED THEN
        UPDATE
        SET SportName    = s.SportName,
            SportId      = s.SportId,
            LeagueName   = s.LeagueName,
            LeagueId     = s.LeagueId,
            Home         = s.Home,
            HomeId       = s.HomeId,
            Away         = s.Away,
            AwayId       = s.AwayId,
            MarketName   = s.MarketName,
            MarketId     = s.MarketId,
            Price        = s.Price,
            Initiator    = s.Initiator,
            Starts       = s.Starts,
            EventId      = s.EventId,
            CheckId      = s.CheckId,
            AccountLogin = s.AccountLogin,
            CheckPrice   = s.CheckPrice,
            CheckStatus  = s.CheckStatus,
            CountLine    = s.CountLine,
            CountEvent   = s.CountEvent,
            AmountEvent  = s.AmountEvent,
            AmountLine   = s.AmountLine,
            MinBet       = s.MinBet,
            MaxBet       = s.MaxBet,
            Balance      = s.Balance,
            Currency     = s.Currency,
            CheckDone    = s.CheckDone,
            CalcStatus   = s.CalcStatus,
            MaxStake     = s.MaxStake,
            MinStake     = s.MinStake,
            MaxWin       = s.MaxWin,
            Stake        = s.Stake,
            Win          = s.Win,
            IsFirst      = s.IsFirst,
            UpdatedAt    = sysdatetimeoffset()

    WHEN NOT MATCHED THEN
        INSERT (Id,
                SideIndex,
                SportName,
                SportId,
                LeagueName,
                LeagueId,
                Home,
                HomeId,
                Away,
                AwayId,
                MarketName,
                MarketId,
                Price,
                Initiator,
                Starts,
                EventId,
                CheckId,
                AccountLogin,
                CheckPrice,
                CheckStatus,
                CountLine,
                CountEvent,
                AmountEvent,
                AmountLine,
                MinBet,
                MaxBet,
                Balance,
                Currency,
                CheckDone,
                CalcStatus,
                MaxStake,
                MinStake,
                MaxWin,
                Stake,
                Win,
                IsFirst)
        VALUES (s.Id,
                s.SideIndex,
                s.SportName,
                s.SportId,
                s.LeagueName,
                s.LeagueId,
                s.Home,
                s.HomeId,
                s.Away,
                s.AwayId,
                s.MarketName,
                s.MarketId,
                s.Price,
                s.Initiator,
                s.Starts,
                s.EventId,
                s.CheckId,
                s.AccountLogin,
                s.CheckPrice,
                s.CheckStatus,
                s.CountLine,
                s.CountEvent,
                s.AmountEvent,
                s.AmountLine,
                s.MinBet,
                s.MaxBet,
                s.Balance,
                s.Currency,
                s.CheckDone,
                s.CalcStatus,
                s.MaxStake,
                s.MinStake,
                s.MaxWin,
                s.Stake,
                s.Win,
                s.IsFirst);
end